export function piApproximation(n){
    switch(n){
        case 1:
            return 3;
        case 2:
            return 3.1;
        case 3:
            return 3.14;
        case 4:
            return 3.141;
        case 5:
            return 3.1415;
        case 6:
            return 3.14159;
        case 7:
            return 3.141592;
        case 8:
            return 3.1415926;
    }
}
